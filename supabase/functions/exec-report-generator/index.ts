/**
 * Executive Report Generator — produces print-ready HTML reports
 * from live KPI data with template-specific sections.
 *
 * POST body: { template: "ops_weekly"|"financial_monthly"|"board_quarterly", facility_id?: string }
 * Auth: JWT via Authorization header (owner/org_admin only).
 */
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.49.1";
import {
  computeKpiForFacilityIds,
  loadFacilitiesForOrganization,
  type ExecKpiPayload,
} from "../_shared/exec-kpi-metrics.ts";
import { getCorsHeaders, jsonResponse } from "../_shared/cors.ts";
import { withTiming } from "../_shared/structured-log.ts";
import { isRateLimited } from "../_shared/rate-limit.ts";

type Template = "ops_weekly" | "financial_monthly" | "board_quarterly" | "custom";
type FacilityKpi = { name: string; id: string; kpi: ExecKpiPayload };

const TEMPLATES: Record<Template, string> = {
  ops_weekly: "Weekly Operations Summary",
  financial_monthly: "Monthly Financial Review",
  board_quarterly: "Quarterly Board Report",
  custom: "Custom Report",
};

// ── HTML Builder Helpers ──

const esc = (s: string) => s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;").replace(/"/g, "&quot;").replace(/'/g, "&#39;");

function kpiCard(label: string, value: string, color = "#0f172a"): string {
  return `<div class="kpi-card"><div class="kpi-label">${esc(label)}</div><div class="kpi-value" style="color:${color}">${esc(value)}</div></div>`;
}

function facilityRow(f: FacilityKpi): string {
  const k = f.kpi;
  const occ = k.census.occupancyPct != null ? `${k.census.occupancyPct}%` : "N/A";
  const ar = `$${(k.financial.totalBalanceDueCents / 100).toLocaleString()}`;
  return `<tr>
    <td><strong>${esc(f.name)}</strong></td>
    <td>${occ}</td>
    <td>${k.clinical.openIncidents}</td>
    <td>${k.clinical.medicationErrorsMtd}</td>
    <td>${ar}</td>
    <td>${k.compliance.openSurveyDeficiencies}</td>
    <td>${k.workforce.certificationsExpiring30d}</td>
    <td>${k.infection.activeOutbreaks}</td>
  </tr>`;
}

