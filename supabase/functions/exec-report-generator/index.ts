// ── Template Renderers ──

function renderOpsWeekly(portfolio: ExecKpiPayload, facilities: FacilityKpi[], alerts: Array<{ severity: string; title: string; body: string | null }>): string {
  const p = portfolio;
  let html = `<div class="kpi-grid">
    ${kpiCard("Occupancy", p.census.occupancyPct != null ? `${p.census.occupancyPct}%` : "N/A", "#10b981")}
    ${kpiCard("Open Incidents", `${p.clinical.openIncidents}`, p.clinical.openIncidents > 0 ? "#ef4444" : "#10b981")}
    ${kpiCard("Med Errors MTD", `${p.clinical.medicationErrorsMtd}`, p.clinical.medicationErrorsMtd > 2 ? "#ef4444" : "#10b981")}
    ${kpiCard("Outbreaks", `${p.infection.activeOutbreaks}`, p.infection.activeOutbreaks > 0 ? "#ef4444" : "#10b981")}
    ${kpiCard("Deficiencies", `${p.compliance.openSurveyDeficiencies}`)}
    ${kpiCard("Certs Expiring", `${p.workforce.certificationsExpiring30d}`)}
  </div>`;
  html += `<h2>Facility Breakdown</h2><table><thead><tr><th>Facility</th><th>Occ %</th><th>Incidents</th><th>Med Errors</th><th>AR</th><th>Deficiencies</th><th>Certs Exp</th><th>Outbreaks</th></tr></thead><tbody>`;
  for (const f of facilities) html += facilityRow(f);
  html += `</tbody></table>`;
  if (alerts.length > 0) {
    html += `<h2>Active Alerts (Top ${alerts.length})</h2>`;
    for (const a of alerts) html += `<div class="alert-row ${a.severity}"><strong>${esc(a.title)}</strong>${a.body ? `<br/>${esc(a.body)}` : ""}</div>`;
  }
  return html;
}

function renderFinancialMonthly(portfolio: ExecKpiPayload, facilities: FacilityKpi[]): string {
  const p = portfolio;
  const ar = `$${(p.financial.totalBalanceDueCents / 100).toLocaleString()}`;
  let html = `<div class="kpi-grid">
    ${kpiCard("Open Invoices", `${p.financial.openInvoicesCount}`, "#6366f1")}
    ${kpiCard("Total AR Outstanding", ar, "#f59e0b")}
    ${kpiCard("Occupancy", p.census.occupancyPct != null ? `${p.census.occupancyPct}%` : "N/A", "#10b981")}
    ${kpiCard("Certs Expiring 30d", `${p.workforce.certificationsExpiring30d}`)}
  </div>`;
  html += `<h2>Facility Financial Summary</h2><table><thead><tr><th>Facility</th><th>Occ %</th><th>Incidents</th><th>Med Errors</th><th>AR</th><th>Deficiencies</th><th>Certs Exp</th><th>Outbreaks</th></tr></thead><tbody>`;
  for (const f of facilities) html += facilityRow(f);
  html += `</tbody></table>`;
  return html;
}

function renderBoardQuarterly(portfolio: ExecKpiPayload, facilities: FacilityKpi[], alerts: Array<{ severity: string; title: string; body: string | null }>): string {
  const p = portfolio;
  const ar = `$${(p.financial.totalBalanceDueCents / 100).toLocaleString()}`;
  let html = `<h2>Executive Summary</h2><div class="kpi-grid">
    ${kpiCard("Portfolio Occupancy", p.census.occupancyPct != null ? `${p.census.occupancyPct}%` : "N/A", "#10b981")}
    ${kpiCard("Licensed Beds", `${p.census.licensedBeds}`)}
    ${kpiCard("Occupied", `${p.census.occupiedResidents}`)}
    ${kpiCard("Total AR", ar, "#f59e0b")}
    ${kpiCard("Open Incidents", `${p.clinical.openIncidents}`, "#ef4444")}
    ${kpiCard("Active Outbreaks", `${p.infection.activeOutbreaks}`, p.infection.activeOutbreaks > 0 ? "#ef4444" : "#10b981")}
    ${kpiCard("Survey Deficiencies", `${p.compliance.openSurveyDeficiencies}`)}
    ${kpiCard("Certs Expiring 30d", `${p.workforce.certificationsExpiring30d}`)}
  </div>`;
  html += `<h2>Facility Scorecard</h2><table><thead><tr><th>Facility</th><th>Occ %</th><th>Incidents</th><th>Med Errors</th><th>AR</th><th>Deficiencies</th><th>Certs Exp</th><th>Outbreaks</th></tr></thead><tbody>`;
  for (const f of facilities) html += facilityRow(f);
  html += `</tbody></table>`;
  if (alerts.length > 0) {
    html += `<h2>Escalation Items</h2>`;
    for (const a of alerts) html += `<div class="alert-row ${a.severity}"><strong>${esc(a.title)}</strong>${a.body ? `<br/>${esc(a.body)}` : ""}</div>`;
  }
  html += `<h2>Strategic Highlights</h2><p style="color:#64748b;font-style:italic">This section is populated by the executive team during board preparation. Use the Haven Scenario Modeling tool for projections.</p>`;
  return html;
}

// ── Main Handler ──

Deno.serve(async (req) => {
  const t = withTiming("exec-report-generator");
  const origin = req.headers.get("origin");

  if (req.method === "OPTIONS") return new Response("ok", { headers: getCorsHeaders(origin) });
  if (req.method !== "POST") return jsonResponse({ error: "Method not allowed" }, 405, origin);

  const url = Deno.env.get("SUPABASE_URL")!;
  const serviceKey = Deno.env.get("SUPABASE_SERVICE_ROLE_KEY")!;
  const admin = createClient(url, serviceKey);

  // Auth
  const token = (req.headers.get("authorization") ?? "").replace("Bearer ", "");
  const { data: { user }, error: authErr } = await admin.auth.getUser(token);
  if (authErr || !user) return jsonResponse({ error: "Unauthorized" }, 401, origin);
  if (isRateLimited(user.id)) return jsonResponse({ error: "Rate limit exceeded." }, 429, origin);

  const { data: profile } = await admin.from("user_profiles").select("organization_id, app_role").eq("id", user.id).maybeSingle();
  if (!profile?.organization_id) return jsonResponse({ error: "Organization not found" }, 403, origin);
  if (!["owner", "org_admin"].includes(profile.app_role)) return jsonResponse({ error: "Insufficient role" }, 403, origin);

  const orgId = profile.organization_id;

  // Parse body
  let body: { template?: string; facility_id?: string };
  try { body = await req.json(); } catch { return jsonResponse({ error: "Invalid JSON" }, 400, origin); }

  const template = (body.template ?? "ops_weekly") as Template;
  if (!TEMPLATES[template]) return jsonResponse({ error: "Invalid template" }, 400, origin);

  // Load data
  const allFacilities = await loadFacilitiesForOrganization(admin, orgId);
  const portfolioKpi = await computeKpiForFacilityIds(admin, orgId, allFacilities);

  const facilityKpis: FacilityKpi[] = [];
  for (const fac of allFacilities) {
    const kpi = await computeKpiForFacilityIds(admin, orgId, [fac]);
    facilityKpis.push({ name: fac.name ?? fac.id.slice(0, 8), id: fac.id, kpi });
  }

  // Load alerts
  const { data: alertRows } = await admin.from("exec_alerts").select("severity, title, body")
    .eq("organization_id", orgId).is("deleted_at", null).is("resolved_at", null)
    .order("severity", { ascending: false }).limit(10);
  const alerts = (alertRows ?? []) as Array<{ severity: string; title: string; body: string | null }>;

  // Render template
  const date = new Date().toLocaleString("en-US", { dateStyle: "long", timeStyle: "short" });
  let sections: string;

  switch (template) {
    case "ops_weekly": sections = renderOpsWeekly(portfolioKpi, facilityKpis, alerts); break;
    case "financial_monthly": sections = renderFinancialMonthly(portfolioKpi, facilityKpis); break;
    case "board_quarterly": sections = renderBoardQuarterly(portfolioKpi, facilityKpis, alerts); break;
    default: sections = renderOpsWeekly(portfolioKpi, facilityKpis, alerts);
  }

  const html = buildHtml(TEMPLATES[template], date, sections);

  t.log({ event: "complete", outcome: "success", template, facilities: allFacilities.length });

  return new Response(JSON.stringify({ ok: true, html, generated_at: new Date().toISOString() }), {
    status: 200,
    headers: {
      ...getCorsHeaders(origin),
      "Content-Type": "application/json",
      "X-Content-Type-Options": "nosniff",
      "Content-Security-Policy": "default-src 'none'; style-src 'unsafe-inline'",
    },
  });
});
